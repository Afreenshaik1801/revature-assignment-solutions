from google.colab import files
uploaded = files.upload()

def find_lengthy_words(file_name, length=7):
    lengthy_words = []
    with open(file_name, 'r') as f:
        for line in f:
            words = line.strip().split()
            lengthy_words.extend([word for word in words if len(word) > length])
    return lengthy_words

long_words = find_lengthy_words('assignment.txt', length=7)
print("Lengthy words (more than 7 letters):")
print(long_words)